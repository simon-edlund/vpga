function interpolate(text, params = {}) {
  return text.replace(/\{(\w+)\}/g, (_, key) => params[key] ?? '')
}

export const useLocaleStore = defineStore('locale', () => {
  function t(key, params = {}) {
    return interpolate(translations.sv[key] ?? key, params)
  }

  return { t }
})
